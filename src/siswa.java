   private void tampil(){
          try {
    String ID = session.getid();           
           String sql = "select*from siswa where nis = '"+ID+"'";
             java.sql.Connection conn = (Connection) koneksi.koneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
        
       txtnis.setText(res.getString(1));
        txtnama.setText(res.getString(2));
        cmbjurusan.getModel().setSelectedItem(res.getString(3));
        cmbkelas.getModel().setSelectedItem(res.getString(4));
            
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }     
    }
