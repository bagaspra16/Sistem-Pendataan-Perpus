    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nis = txtnis.getText();
    String nama = txtnama.getText();
      if(nis.equals("")||nama.equals("")){
            JOptionPane.showMessageDialog(null,"Isi Semua Form Yang Tersedia ");
        
    }
    else if(cmbjurusan.getSelectedItem().equals("Jurusan")||cmbkelas.getSelectedItem().equals("Kelas")){
            JOptionPane.showMessageDialog(null,"Pilih jurusan atau kelas ");
        }
        else{
        try {
            String query ="UPDATE siswa SET nis = '"+txtnis.getText()
                    +"',nama = '"+txtnama.getText()
                    +"', jurusan = '"+cmbjurusan.getSelectedItem()
                    +"',kelas = '"+cmbkelas.getSelectedItem()
                    +"' WHERE nis = '"+txtnis.getText()+"'";
            java.sql.Connection kon=(Connection)koneksi.koneksi();
            java.sql.PreparedStatement pdt=kon.prepareStatement(query);
            pdt.execute();
            JOptionPane.showMessageDialog(null, "data berhasil di edit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
        }
        new datasiswa().setVisible(true);
dispose();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

